#include "libft.h"

int	ft_isalnum(int c)
{
	int	isalnum;

	isalnum = 1;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')
		|| (c >= '0' && c <= '9'))
		return (isalnum);
	return (0);
}
